package Animales;

public class Perro extends Canido{
    
    public String AnimalSonido() {  //Metodo que devuelve el sonido de un Perro
        return "Ladrido";
    }

    public String AnimalDieta() { //Metodo que devuelve la alimentacion de un Perro
        return "Carnivoro";
    }

    public String AnimalHabitat() { //Metodo que devuelve la vivienda de un Perro
        return "Domestico";
    }

    public String AnimalNombreCientifico() { //Metodo que devuelve el nombre cientifico de un Perro
        return "Canis lupus familiaris";
    }
}
    

