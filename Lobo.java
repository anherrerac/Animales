package Animales;

public class Lobo extends Canido{

    public String AnimalSonido() {  //Metodo que devuelve el sonido de un Lobo
        return "Aullido";
    }

    public String AnimalDieta() {  //Metodo que devuelve la alimentacion de un Lobo
        return "Carnivoro";
    }

    public String AnimalHabitat() { //Metodo que devuelve la vivienda de un Lobo
        return "Bosque";
    }

    public String AnimalNombreCientifico() { //Metodo que devuelve el nombre cientifico de un Lobo
        return "Canis lupus";
    }
}